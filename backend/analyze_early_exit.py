stop_loss_threshold = 5.0  # 默认止损线5%
leverage = 5  # 杠杆倍数

for i, trade in enumerate(trades, 1):
    print(f"{i}. {trade['币种']}")
    print(f"   开仓: {trade['开仓价']} → 平仓: {trade['平仓价']}")
    print(f"   亏损: {trade['亏损百分比']:.2f}%")

    # 计算币价变化
    price_change = (trade['平仓价'] - trade['开仓价']) / trade['开仓价'] * 100
    print(f"   币价变化: {price_change:.2f}%")

    # 计算保证金收益率(考虑杠杆)
    margin_pnl = price_change * leverage
    print(f"   保证金收益率(5x杠杆): {margin_pnl:.2f}%")

    # 判断是否达到止损线
    is_stop_loss = margin_pnl <= -stop_loss_threshold

    if is_stop_loss:
        print(f"   ✅ 触发止损: {margin_pnl:.2f}% <= -{stop_loss_threshold}%")
    else:
        print(f"   ❌ 未达止损: {margin_pnl:.2f}% > -{stop_loss_threshold}%")
        print(f"   ⚠️  提前平仓!")

    # 计算持仓时长
    from datetime import datetime
    open_time = datetime.strptime(trade['开仓时间'], "%Y/%m/%d %H:%M:%S")
    close_time = datetime.strptime(trade['平仓时间'], "%Y/%m/%d %H:%M:%S")
    duration = (close_time - open_time).total_seconds() / 60
    print(f"   持仓时长: {duration:.1f} 分钟")

    print()

